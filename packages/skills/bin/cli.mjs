function getAvailableSkills() {
  return DISTRIBUTED_SKILLS.filter((skillName) => existsSync(join(getSkillSourceDir(skillName), "SKILL.md")));
}

function getSkillMetadata(skillName) {
  const skillPath = join(getSkillSourceDir(skillName), "SKILL.md");
  if (!existsSync(skillPath)) return null;
  const content = readFileSync(skillPath, "utf8");
  // Parse YAML frontmatter
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return { name: skillName, description: "" };
  const yaml = match[1];
  const name = yaml.match(/^name:\s*(.+)$/m)?.[1]?.trim() || skillName;
  const desc = yaml.match(/description:\s*>\s*\n([\s\S]*?)(?=\n\w|\n---)/)?.[1]?.trim()
    || yaml.match(/description:\s*(.+)$/m)?.[1]?.trim() || "";
  return { name, description: desc.replace(/\n\s*/g, " ") };
}

function getSkillSourceDir(skillName) {
  return join(PACKAGED_SKILLS_DIR, skillName);
}

function isManagedFile(filePath) {
  return existsSync(filePath) && readFileSync(filePath, "utf8").includes(MANAGED_MARKER);
}

function ensureParentDir(filePath) {
  mkdirSync(dirname(filePath), { recursive: true });
}

function wrapManagedContent(body, frontmatter = "") {
  if (frontmatter) {
    return `${frontmatter.trim()}\n${MANAGED_MARKER}\n\n${body.trim()}\n`;
  }
  return `${MANAGED_MARKER}\n\n${body.trim()}\n`;
}

function getSkillTargetDir(targetDir, platformName, skillName) {
  return join(targetDir, SKILL_TARGETS[platformName], skillName);
}

function getInstalledSkillCount(targetDir, platformName, availableSkills) {
  if (!SKILL_TARGETS[platformName]) return 0;
  return availableSkills.filter((skill) => existsSync(getSkillTargetDir(targetDir, platformName, skill))).length;
}

function installSkill(skillName, targetDir, platformName, force) {
  const srcDir = getSkillSourceDir(skillName);
  if (!existsSync(join(srcDir, "SKILL.md"))) {
    console.error(`  ✗ Skill "${skillName}" not found`);
    return false;
  }

  const destDir = getSkillTargetDir(targetDir, platformName, skillName);
  const destFile = join(destDir, "SKILL.md");

  if (existsSync(destFile) && !force) {
    console.log(`  ⊘ ${SKILL_TARGETS[platformName]}/${skillName} (exists, use --force to overwrite)`);
    return false;
  }

  mkdirSync(dirname(destDir), { recursive: true });
  if (existsSync(destDir)) {
    rmSync(destDir, { recursive: true, force: true });
  }
  cpSync(srcDir, destDir, { recursive: true });
  console.log(`  ✓ ${platformName}/${skillName}`);
  return true;
}

function removeManagedFile(targetDir, relativePath) {
  const filePath = join(targetDir, relativePath);
  if (!existsSync(filePath)) return false;
  if (!isManagedFile(filePath)) {
    console.log(`  ⊘ ${relativePath} (exists and is not managed by @contextro/skills)`);
    return false;
  }
  rmSync(filePath, { force: true });
  console.log(`  ✓ Removed ${relativePath}`);
  return true;
}

function writeManagedFile(targetDir, relativePath, content, force) {
  const filePath = join(targetDir, relativePath);
  if (existsSync(filePath) && !force) {
    if (isManagedFile(filePath)) {
      console.log(`  ⊘ ${relativePath} (exists, use --force to overwrite)`);
    } else {
      console.log(`  ⊘ ${relativePath} (exists and is not managed by @contextro/skills)`);
    }
    return false;
  }

  ensureParentDir(filePath);
  writeFileSync(filePath, content, "utf8");
  console.log(`  ✓ ${relativePath}`);
  return true;
}

