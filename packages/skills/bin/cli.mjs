// --- Main ---

const args = process.argv.slice(2);
if (args.includes("--help") || args.includes("-h") || args.length === 0) {
  usage();
  process.exit(0);
}
if (args.includes("--version") || args.includes("-v")) {
  console.log(VERSION);
  process.exit(0);
}

const command = args[0];
const flags = {};
const positional = [];

for (let i = 1; i < args.length; i++) {
  if (args[i] === "--dir") { flags.dir = args[++i]; }
  else if (args[i] === "--platform") { flags.platform = args[++i]; }
  else if (args[i] === "--force") { flags.force = true; }
  else if (!args[i].startsWith("-")) { positional.push(args[i]); }
}

const targetDir = resolve(flags.dir || process.cwd());
const platforms = resolvePlatforms(flags.platform);

switch (command) {
  case "install": {
    const available = getAvailableSkills();
    const toInstall = positional.length > 0 ? positional : available;
    const invalid = toInstall.filter((s) => !available.includes(s));
    if (invalid.length) {
      console.error(`Unknown skills: ${invalid.join(", ")}`);
      console.error(`Available: ${available.join(", ")}`);
      process.exit(1);
    }
    console.log(`Installing ${toInstall.length} skill(s) into ${targetDir}\n`);
    let artifactCount = 0;
    for (const platform of platforms) {
      artifactCount += installPlatform(platform, toInstall, targetDir, flags.force);
    }
    console.log(`\n✓ Done. ${artifactCount} artifact(s) updated.`);
    break;
  }

  case "list": {
    const available = getAvailableSkills();
    if (!available.length) {
      console.log("No skills available.");
      break;
    }
    console.log(`Available skills (${available.length}):\n`);
    for (const name of available) {
      const meta = getSkillMetadata(name);
      console.log(`  ${name}`);
      if (meta?.description) console.log(`    ${meta.description.slice(0, 80)}`);
    }
    break;
  }

  case "info": {
    if (!positional[0]) { console.error("Usage: info <skill>"); process.exit(1); }
    const meta = getSkillMetadata(positional[0]);
    if (!meta) { console.error(`Skill "${positional[0]}" not found`); process.exit(1); }
    console.log(`Skill: ${meta.name}`);
    console.log(`Description: ${meta.description}`);
    const content = readFileSync(join(getSkillSourceDir(positional[0]), "SKILL.md"), "utf8");
    console.log(`\n${content}`);
    break;
  }

  case "uninstall": {
    if (!positional.length) { console.error("Usage: uninstall <skill...>"); process.exit(1); }
    const available = getAvailableSkills();
    let removed = 0;
    for (const platform of platforms) {
      removed += uninstallPlatform(platform, positional, targetDir, available);
    }
    console.log(`\n✓ Done. ${removed} artifact(s) removed.`);
    break;
  }

  case "benchmark": {
    console.log("MCP vs no-MCP benchmark runner");
    console.log("See: docs/EXPERIMENT_FRAMEWORK.md for methodology.\n");
    const codebase = flags.dir || process.cwd();
    try {
      execSync(
        `node "${join(__dirname, "experiment.mjs")}" --codebase "${codebase}"`,
        { stdio: "inherit", env: { ...process.env } }
      );
    } catch (e) {
      process.exit(e.status || 1);
    }
    break;
  }

  case "plugin": {
    const PLUGIN_TARGETS = ["claude-code", "codex", "kiro"];
    const target = positional[0];
    if (!target || !PLUGIN_TARGETS.includes(target)) {
      console.error(`Usage: plugin <${PLUGIN_TARGETS.join("|")}>`);
      console.error(`Generates a plugin package for the specified agent target.`);
      process.exit(1);
    }
    const outDir = resolve(flags.dir || process.cwd(), `contextro-plugin-${target}`);
    console.log(`Generating ${target} plugin at ${outDir}\n`);
    generatePlugin(target, outDir, flags.force);
    console.log(`\n✓ Plugin generated at ${outDir}`);
    break;
  }

  default:
    console.error(`Unknown command: ${command}`);
    usage();
    process.exit(1);
}
