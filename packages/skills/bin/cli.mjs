function installGitHubArtifacts(targetDir, force) {
  const repoWidePath = join(targetDir, GITHUB_REPO_INSTRUCTIONS);
  if (!existsSync(repoWidePath) || isManagedFile(repoWidePath) || force) {
    return writeManagedFile(targetDir, GITHUB_REPO_INSTRUCTIONS, renderGitHubInstructions(), force) ? 1 : 0;
  }

  console.log(`  ⊘ ${GITHUB_REPO_INSTRUCTIONS} (exists and is not managed by @contextro/skills)`);
  return writeManagedFile(targetDir, GITHUB_FALLBACK_INSTRUCTIONS, renderGitHubFallbackInstructions(), force) ? 1 : 0;
}

function installCodexArtifacts(targetDir, force) {
  let count = 0;
  if (writeManagedFile(targetDir, CODEX_GUIDE_FILE, renderCodexGuide(), force)) count++;

  const agentsPath = join(targetDir, CODEX_AGENTS_FILE);
  if (!existsSync(agentsPath) || isManagedFile(agentsPath) || force) {
    if (writeManagedFile(targetDir, CODEX_AGENTS_FILE, renderCodexAgents(), force)) count++;
  } else {
    console.log(`  ⊘ ${CODEX_AGENTS_FILE} (exists and is not managed by @contextro/skills)`);
  }

  return count;
}

function installPlatform(platformName, skills, targetDir, force) {
  let updated = 0;

  if (SKILL_TARGETS[platformName]) {
    for (const skill of skills) {
      if (installSkill(skill, targetDir, platformName, force)) updated++;
    }
  }

  if (platformName === "github") {
    updated += installGitHubArtifacts(targetDir, force);
  }
  if (platformName === "codex") {
    updated += installCodexArtifacts(targetDir, force);
  }

  return updated;
}

function uninstallPlatform(platformName, skills, targetDir, availableSkills) {
  let removed = 0;

  if (SKILL_TARGETS[platformName]) {
    for (const skill of skills) {
      const destDir = getSkillTargetDir(targetDir, platformName, skill);
      if (!existsSync(destDir)) continue;
      rmSync(destDir, { recursive: true, force: true });
      console.log(`  ✓ Removed ${platformName}/${skill}`);
      removed++;
    }
  }

  if (platformName === "github" && getInstalledSkillCount(targetDir, "github", availableSkills) === 0) {
    if (removeManagedFile(targetDir, GITHUB_REPO_INSTRUCTIONS)) removed++;
    if (removeManagedFile(targetDir, GITHUB_FALLBACK_INSTRUCTIONS)) removed++;
  }

  if (platformName === "codex") {
    if (removeManagedFile(targetDir, CODEX_AGENTS_FILE)) removed++;
    if (removeManagedFile(targetDir, CODEX_GUIDE_FILE)) removed++;
  }

  return removed;
}

function resolvePlatforms(platformArg) {
  if (!platformArg) return INSTALL_TARGETS;
  const map = {
    claude: "claude",
    agents: "agents",
    agent: "agents",
    github: "github",
    codex: "codex",
    kiro: "kiro",
    opencode: "opencode",
  };
  const p = map[platformArg];
  if (!p) {
    console.error(`Unknown platform: ${platformArg}. Use: claude, agent, github, codex, kiro, opencode`);
    process.exit(1);
  }
  return [p];
}

