/**
 * @contextro/skills CLI
 *
 * Distribute the packaged dev-contextro-mcp skill bundle and the smallest
 * repo-local instruction files needed for supported coding agents.
 *
 * Taxonomy:
 * - skill source: packages/skills/skills/dev-contextro-mcp/ (SKILL.md + references/ + evals/)
 * - skill targets: .claude/skills, .agents/skills, .github/skills, .kiro/skills, .opencode/skills
 * - instruction targets: GitHub Copilot instructions and Codex-style AGENTS.md/docs
 *
 * This CLI does not build vendor plugin packages.
 * Usage: npx @contextro/skills [command] [options]
 */

